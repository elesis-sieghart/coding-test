SELECT BYE.ANIMAL_ID, BYE.NAME 
FROM ANIMAL_OUTS BYE, ANIMAL_INS HI
WHERE BYE.ANIMAL_ID=HI.ANIMAL_ID
AND HI.DATETIME > BYE.DATETIME
ORDER BY HI.DATETIME;

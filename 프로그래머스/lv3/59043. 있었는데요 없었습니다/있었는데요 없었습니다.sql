SELECT BYE.ANIMAL_ID, BYE.NAME 
FROM ANIMAL_OUTS BYE
JOIN ANIMAL_INS HI
ON BYE.ANIMAL_ID=HI.ANIMAL_ID
WHERE HI.DATETIME > BYE.DATETIME
ORDER BY HI.DATETIME;

